# Merge feature usage with subscriptions
feature_join = feature_usage.merge(
    subscriptions[["subscription_id", "account_id"]],
    on="subscription_id",
    how="left"
)

feature_join.head()

feature_join.columns

# Aggregate feature usage by account

feature_agg = feature_join.groupby("account_id").agg(
    total_usage=("usage_count", "sum"),
    avg_usage_duration=("usage_duration_secs", "mean"),
    total_errors=("error_count", "sum"),
    beta_feature_used=("is_beta_feature", "max")
).reset_index()

feature_agg.head()

# Aggregate support tickets by account

support_agg = support.groupby("account_id").agg(
    ticket_count=("ticket_id", "count"),
    avg_resolution_time=("resolution_time_hours", "mean"),
    avg_first_response=("first_response_time_minutes", "mean"),
    avg_satisfaction=("satisfaction_score", "mean"),
    escalations=("escalation_flag", "sum")
).reset_index()

support_agg.head()

# Merge all datasets

merged = accounts.merge(
    feature_agg,
    on="account_id",
    how="left"
)

merged = merged.merge(
    support_agg,
    on="account_id",
    how="left"
)

merged = merged.merge(
    subscriptions,
    on="account_id",
    how="left"
)

merged.fillna(0, inplace=True)

